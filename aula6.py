# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis  primitivos:
# str, int, float, bool

print(int('1'), type(int('1'))) # conversão de tipos
print(int('1') + 1) # conversão de tipos
print(float('1.0') + 1) # conversão de tipos
print(bool(' ')) # conversão de tipos
print(str(1) + 'b') # conversão de tipos
"""
python = linguagem de programação
tipo de tipagem = dinâmica / forte
str -> string -> texto
strings são textos que estão dentro de aspas
"""

# aspas simples
print('olá')

# aspas duplas
print("olá")

# escape
print("olá \"mundo\"") # a barra serve para escapar do interpretador de fechar a string, assim podemos colocar aspas no texto caso necessário...

# r
print(r"olá \"mundo\"")

print(1, "olá, 'mundo'")

# tipos int e float
# int -> número inteiro
# o tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo.

# float -> número com ponto flutuante
# o tipo float respresenta qualquer número 
# positivo ou negativo com ponto flutuant.
# float sem sinal é considerado positivo.

print(10) # int
print(-10) # int
print(0) #int

print(1.1) # float
print(0.0) # float
print(-1.1) # float

# a função type mostra o tipo que o python inferiu ao valor.

print(type('paulo'))
print(type(1))
print(type(1.0), type('olá'))
